from services import admin_helpers, context, question_selection


def build(
    *,
    engine,
    flask_runtime,
    render_template,
    recent_audit,
    json_dumps,
    perf_counter,
    work_title,
    safe_work_url,
    use_db,
    amazon_associate_id='',
    matrix_ops,
    cleanup_sessions,
    player_fetish_base_id,
    strftime,
    gmtime,
    parse_works_list,
    list_compound_works,
    set_compound_works,
    delete_compound_works,
    write_audit,
    filesystem,
    share_event_report,
    question_event_report,
    share_event_count,
    question_event_count,
    share_event_storage_status,
    question_event_storage_status,
    load_share_notes,
    save_share_note,
    enable_test_play,
    disable_test_play,
    is_test_play,
):
    request = flask_runtime.request
    jsonify = flask_runtime.jsonify
    response_cls = flask_runtime.response_cls
    session = flask_runtime.session
    environ = flask_runtime.environ
    runtime = context.admin_runtime(
        engine=engine,
        request=request,
        jsonify=jsonify,
        Response=response_cls,
        render_template=render_template,
        session=session,
        csrf_token=flask_runtime.csrf_token,
        recent_audit=recent_audit,
        json_dumps=json_dumps,
        environ=environ,
        require_confirm=flask_runtime.require_confirm,
    )
    reporting = context.admin_reporting(
        bounded_int=admin_helpers.bounded_int,
        build_fetish_log_rows=lambda: admin_helpers.build_fetish_log_rows(engine),
        build_completion_metrics=admin_helpers.build_completion_metrics,
        paged_fetish_log_rows=admin_helpers.paged_fetish_log_rows,
        perf_counter=perf_counter,
        best_question=question_selection.best_question,
        build_admin_maintenance_checklist=admin_helpers.make_admin_maintenance_checklist(
            engine, work_title, safe_work_url,
        ),
        use_db=use_db,
        list_matrix_import_backups=matrix_ops.list_backups,
        should_enforce_runtime_guard=flask_runtime.should_enforce_runtime_guard,
        cleanup_sessions=cleanup_sessions,
        player_fetish_base_id=player_fetish_base_id,
        strftime=strftime,
        gmtime=gmtime,
        share_event_report=share_event_report,
        question_event_report=question_event_report,
        share_event_count=share_event_count,
        question_event_count=question_event_count,
        share_event_storage_status=share_event_storage_status,
        question_event_storage_status=question_event_storage_status,
        load_share_notes=load_share_notes,
        save_share_note=save_share_note,
        write_audit=write_audit,
        enable_test_play=enable_test_play,
        disable_test_play=disable_test_play,
        is_test_play=is_test_play,
    )
    maintenance = context.admin_maintenance(
        parse_works_list=parse_works_list,
        work_title=work_title,
        safe_work_url=safe_work_url,
        amazon_associate_id=amazon_associate_id,
        list_compound_works=list_compound_works,
        set_compound_works=set_compound_works,
        delete_compound_works=delete_compound_works,
        write_audit=write_audit,
        load_json_file=filesystem.load_json_file,
        data_path=filesystem.data_path,
    )
    matrix_tools = context.admin_matrix_tools(
        app_dir=filesystem.app_dir,
        relpath=filesystem.relpath,
        basename=filesystem.basename,
        join_path=filesystem.join_path,
        path_exists=filesystem.path_exists,
        re_search=filesystem.re_search,
        html_escape=filesystem.html_escape,
        snapshot_current_matrix=matrix_ops.snapshot_current_matrix,
        matrix_import_completeness_error=matrix_ops.completeness_error,
        matrix_import_expected_rows=matrix_ops.expected_rows,
    )
    return context.build_admin_context(runtime, reporting, maintenance, matrix_tools)
